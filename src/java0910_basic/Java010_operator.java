package java0910_basic;

public class Java010_operator {

	public static void main(String[] args) {
		int x = 5;
		int y = 3;
		int z = 5;

		// x가 y보다 크다.
		System.out.println(x > y); // true

		// x가 y보다 작다.
		System.out.println(x < y); // false

		// x가 z보다 크거나 같다.
		System.out.println(x >= z); // true

		// x가 z보다 작거나 같다.
		System.out.println(x <= z); // true

		// x와 z가 같다.
		System.out.println(x == z); // true

		// x와 z가 같지 않다.
		System.out.println(x != z); // false

	}

}
